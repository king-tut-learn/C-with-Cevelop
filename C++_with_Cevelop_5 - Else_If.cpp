#include <iostream>
using namespace std;

int main() {

	int x = 9;

	//other comparison signs
	//==, <=, >=, <, and >
	if (x==8)
	{
		cout << "X matches with a number assigned to it!!" << endl;
	}

	else if (x == 9)
	{
		cout << "Almost there!!" << endl;
	}

	else if (x == 7)
	{
		cout << "Lower than 8!!" << endl;
	}

	else
	{
		cout << "X does not match!!" << endl;
	}

	return 0;
}
